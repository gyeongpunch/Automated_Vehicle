function [ v ] = road_curve( u )

%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
% input
% u(1) : x position of road (m)
%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
% output
% v(1) : y position of road (m)
%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
% prameter
% cuv : curvature gain
% 
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%

x_p = u(1);

cuv = 60;

if(x_p <= 200)
    y_p = 0;
    
else   % (x_p >200)
    y_p = 100* cos((x_p-200)/(cuv*pi)) -100;

end

v(1) = y_p;


    